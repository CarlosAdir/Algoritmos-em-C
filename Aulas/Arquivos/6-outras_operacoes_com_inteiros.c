#include <stdio.h>

int main()
{
	int num1 = 40 , num2 = 5;
	printf ( " O resultado da subtracao e : % d " , num1 - num2 ) ; /* 35 */
	printf ( " O resultado da multiplicacao e : % d " , num1 * num2 ) ; /* 200 */
	printf ( " O resultado da divisao e : % d " , num1 / num2 ) ; /* 8 */
	return 0;
}
