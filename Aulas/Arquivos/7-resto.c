#include <stdio.h>

int main()
{
	printf ( " 7%%2 = %d\n" , 7%2) ; /* 1 */
	printf ( " 5%%3 = %d\n" , 5%3) ; /* 2 */
	printf ( " 7%%8 = %d\n" , 7%8) ; /* 7 */
	printf ( " 1%%3 = %d\n" , 1%2) ; /* 1 */
	printf ( " 2%%3 = %d\n" , 2%3) ; /* 2 */
	printf ( " 3%%3 = %d\n" , 3%8) ; /* 0 */
	printf ( " 4%%3 = %d\n" , 4%2) ; /* 1 */
	printf ( " 5%%3 = %d\n" , 5%3) ; /* 2 */
	printf ( " 6%%3 = %d\n" , 6%8) ; /* 0 */
	return 0;
}
